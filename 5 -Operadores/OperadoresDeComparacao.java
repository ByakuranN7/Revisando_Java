public class OperadoresDeComparacao{
	public static void main(String[] args){
		int a = 10;
		int b = 20;

		System.out.println(a == b); //true if igual
		System.out.println(a != b); //true if diferente
		System.out.println(a > b); 
		System.out.println(a < b); 
		System.out.println(a >=b); //true if a for maior ou igual a b
		System.out.println(a <=b); //true if a menor ou igual a b
	}
}