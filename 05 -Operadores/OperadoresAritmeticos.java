public class OperadoresAritmeticos{
	public static void main(String[] args){
		int a = 10;
		int b = 2;

		System.out.println("\n" + (a + b)); //soma
		System.out.println(a - b); //subtração
		System.out.println(a * b); //multiplicação
		System.out.println(a / b); //divisão
		System.out.println(a % b); //módulo (resto da divisão), nesse caso é 0 o resultado
	}
}